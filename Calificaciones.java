public class Calificaciones{
	String materia;
	int calificacion;

	public void setMateria(String mat){
		materia = mat;
	}

	public void setCalificacion(int cal){
		calificacion = cal;
	}

	public String getMateria(){
		return materia;
	}

	public int getCalificacion(){
		return calificacion;
	}
}
